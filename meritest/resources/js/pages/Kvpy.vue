                                    <div class="section-title text-left mb-20">
                                        <h3>Facts, Syllabus and Pattern</h3>
                                    </div>
                                    <p>The Kishore Vaigyanik Protsahan Yojana (KVPY) is an on-going National Program of Fellowship in Basic Sciences, initiated and funded by the Department of Science and Technology, 
Government of India, to attract exceptionally highly motivated students for pursuing basic science courses and research career in science.</p>
                                    <p>The advertisement for the KVPY Fellowship appears in all the national dailies normally on the Technology Day (May 11) and the Second Sunday of July every year.</p>
                                    <p>The Indian Institute of Science administers KVPY to offer scholarship and contingency grants up to Pre Ph.D. for select students.</p>
                                    <h5 class="spacing-10 font600 text-org">The objective of the program is as follows:</h5>
                                    <ul class="ml-15 list-group list-with-icon">
                                        <li>Identify students with talent and aptitude for research</li>
                                        <li>Help them realize their academic potential</li>
                                        <li>Encourage them to take up research careers in Science</li>
                                        <li>Ensure the growth of the best scientific minds for research and development in the country.</li>
                                    </ul>

                                    <h5 class="spacing-10 font600 text-org">KVPY has the following streams:</h5>
                                    <ul class="ml-15 list-group list-with-icon">
                                        <li>SA: Class 11 Students can appear in this stream</li>
                                        <li>SX: Class 12 Students can appear in this stream</li>
                                        <li>SB: 1st year of any undergraduate Program in Basic Sciences namely B.Sc./B.S./B.Stat./B.Math. /Int. M.Sc./M.S. in Mathematics, Physics, Chemistry and Biology.</li>
                                    </ul>
                                    <p>If students clear the written exam and interview, they are then eligible for admissions to institutes like IISc or IISER.</p>
        
                                    
