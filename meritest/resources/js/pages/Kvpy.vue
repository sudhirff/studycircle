                                    <div class="section-title text-left mb-20">
                                        <h3>About KVPY</h3>
                                    </div>
                                    <p>The "Kishore Vaigyanik Protsahan Yojana" (KVPY) is a program started in 1999 by the Department of Science and Technology (DST), Government of India to encourage students who are 
studying Basic Sciences to take up research career in Science. The aim of the program is to identify and encourage talented and motivated students to pursue career in research</p>										
                                    <p>This program aims to assist the students to realize their potential and to ensure that the best scientific talent is groomed for research and development in the country. Generous fellowship and 
contingency grant are provided to the selected KVPY Fellows up to the pre Ph.D. level or 5 years whichever is earlier. In addition, summer camps for the KVPY Fellows are organized in 
prestigious research and educational institutions in the country.
</p>
                                    <p>The Department of Science and Technology, the nodal agency of the Government has entrusted the overall responsibility for organizing and running the KVPY Program to the Indian Institute of 
Science, Bangalore and set up a Management Committee and a National Advisory Committee (NAC) for overseeing its implementation. A core committee looks after both the day-to-day and 
academic aspects of the KVPY Program</p>
